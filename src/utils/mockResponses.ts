// تابع اصلی: پیدا کردن پاسخ مناسب
export function getMockResponse(userMessage: string): string {
  const lowerMessage = userMessage.toLowerCase()

  // دنبال کلمات کلیدی بگرد
  for (const item of KEYWORD_RESPONSES) {
    for (const keyword of item.keywords) {
      if (lowerMessage.includes(keyword)) {
        return item.response
      }
    }
  }

  // اگه هیچ کلمه کلیدی پیدا نشد → پاسخ تصادفی
  const randomIndex = Math.floor(Math.random() * DEFAULT_RESPONSES.length)
  return DEFAULT_RESPONSES[randomIndex]
}